int main() {

    // set UTF8 di windows
    #ifdef _WIN32
        SetConsoleOutputCP(CP_UTF8);
        SetConsoleCP(CP_UTF8);
    #else
        setlocale(LC_ALL, "");
    #endif

    std::srand(static_cast<unsigned>(std::time(nullptr)));  // seed RNG

    std::cout << "╔═══════════════════════════════╗\n";
    std::cout << "║     T A M A G O T C H I       ║\n";
    std::cout << "║        C + +  ver.idk         ║\n";
    std::cout << "╚═══════════════════════════════╝\n\n";

    // 3. POINTER — dynamic object ownership
    Tamagotchi* pet = nullptr;

    // Offer load if a save file exists
    if (TamagotchiEngine::saveExists()) {
        std::cout << "[!] Save file found. Load previous game? (y/n): ";
        char answer;
        std::cin >> answer;
        std::cin.ignore();
        if (answer == 'y' || answer == 'Y') {
            pet = TamagotchiEngine::loadGame();
        }
    }

    // Create new pet if not loading
    if (pet == nullptr) {
        std::string petName;
        std::cout << "Enter your Tamagotchi's name: ";
        std::getline(std::cin, petName);
        if (petName.empty()) petName = "Tama";
        pet = TamagotchiEngine::createTamagotchi(petName);
        std::cout << "\nWelcome, " << pet->name << "! \n";
    } else {
        std::cout << "\nWelcome back, " << pet->name << "! \n";
    }

    TamagotchiEngine::display(*pet);

    int choice = -1;
    while (choice != 0 && Utils::isAlive(*pet)) {
        printMenu();
        std::cin >> choice;

        // 5. CALLBACK — action passed as function to applyAction
        switch (choice) {
            case 1:
                TamagotchiEngine::applyAction(*pet, TamagotchiEngine::feedAction,  "FEED");
                std::cout << pet->name << " eats happily! \n";
                break;
            case 2:
                TamagotchiEngine::applyAction(*pet, TamagotchiEngine::playAction,  "PLAY");
                std::cout << pet->name << " plays joyfully! \n";
                break;
            case 3:
                TamagotchiEngine::applyAction(*pet, TamagotchiEngine::sleepAction, "SLEEP");
                std::cout << pet->name << " sleeps soundly... \n";
                break;
            case 4:
                TamagotchiEngine::applyAction(*pet, TamagotchiEngine::healAction,  "HEAL");
                std::cout << pet->name << " takes medicine \n";
                break;
            case 5:
                // Overload 1 — full object
                TamagotchiEngine::display(*pet);
                // Overload 2 — single stat
                TamagotchiEngine::display("Fullness", 100 - pet->hunger);
                break;
            case 6:
                TamagotchiEngine::showLogs(*pet, 8);
                break;
            case 7:
                TamagotchiEngine::applyAction(*pet, TamagotchiEngine::ageTick, "TIME");
                std::cout << "\nTime passes... now on day " << pet->age << "\n";
                TamagotchiEngine::triggerRandomEncounter(*pet);
                TamagotchiEngine::display(*pet);
                break;
            case 8:
                TamagotchiEngine::saveGame(*pet);
                break;
            case 9: {
                Tamagotchi* loaded = TamagotchiEngine::loadGame();
                if (loaded != nullptr) {
                    delete pet;       // free old object
                    pet = loaded;     // replace with loaded data
                    TamagotchiEngine::display(*pet);
                }
                break;
            }
            case 0:
                std::cout << "Goodbye!\n";
                break;
            default:
                std::cout << "Invalid choice.\n";
        }

        if (!Utils::isAlive(*pet)) {
            std::cout << "\n Oh no! " << pet->name << " has passed away...\n";
            std::cout << "They survived for " << pet->age << " days.\n";
            TamagotchiEngine::showLogs(*pet, 10);
        }
    }

    // 3. POINTER — free memory
    delete pet;
    pet = nullptr;

    std::cout << "\nProgram ended. Thank you!\n";
    return 0;
}

