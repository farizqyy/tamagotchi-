    // Pick and run one random encounter
    // Returns true if an encounter occurred
    bool triggerRandomEncounter(Tamagotchi& t) {
        // Check whether an encounter happens this turn
        if ((std::rand() % 100) >= ENCOUNTER_CHANCE) return false;

        // Pick a random event from the table
        const EncounterEvent& ev =
            ENCOUNTER_TABLE[std::rand() % ENCOUNTER_COUNT];

        std::cout << "\n  ╔════════════════════════════════════════╗\n";
        std::cout << "  ║             RANDOM ENCOUNTER!          ║\n";
        std::cout << "  ╠════════════════════════════════════════╣\n";
        std::cout << "  ║  " << std::left << std::setw(36) << ev.description << " ║\n";
        std::cout << "  ╠════════════════════════════════════════╣\n";

        // Print only stats that change
        auto printDelta = [](const std::string& label, int delta) {
            if (delta == 0) return;
            std::cout << "  ║  " << std::left << std::setw(12) << label
                      << ": " << (delta > 0 ? "+" : "") << std::setw(4) << delta
                      << std::setw(18) << " " << "  ║\n";
        };
        // Hunger displayed as "Fullness" (inverted)
        printDelta("Fullness",  -ev.hungerDelta);
        printDelta("Happiness",  ev.happinessDelta);
        printDelta("Energy",     ev.energyDelta);
        printDelta("Health",     ev.healthDelta);
        std::cout << "  ╚════════════════════════════════════════╝\n";

        // 5. CALLBACK — pass lambda as modifier
        applyEncounter(t, ev, [](Tamagotchi& pet, const EncounterEvent& e) {
            pet.hunger    = clamp(pet.hunger    + e.hungerDelta,    0, 100);
            pet.happiness = clamp(pet.happiness + e.happinessDelta, 0, 100);
            pet.energy    = clamp(pet.energy    + e.energyDelta,    0, 100);
            pet.health    = clamp(pet.health    + e.healthDelta,    0, 100);
        });

        return true;
    }

    // Natural aging — called each turn
    void ageTick(Tamagotchi& t) {
        t.age++;
        t.hunger    = clamp(t.hunger + 10,    0, 100);
        t.happiness = clamp(t.happiness - 5,  0, 100);
        t.energy    = clamp(t.energy - 8,     0, 100);
        // If starving or unhappy, health degrades
        if (t.hunger >= 80) t.health = clamp(t.health - 15, 0, 100);
        if (t.happiness <= 20) t.health = clamp(t.health - 5, 0, 100);

        Utils::addLog(t, "TIME",
            "Day " + std::to_string(t.age) +
            " | Hunger +10 | Happiness -5 | Energy -8");
    }

    // ─────────────────────────────────────
    // 7a. FUNCTION OVERLOADING — display()
    // ─────────────────────────────────────

    // Overload 1: display full Tamagotchi status
    void display(const Tamagotchi& t) {
        std::cout << "\n╔═════════════════════════════════════════╗\n";
        std::cout << "║      " << std::left << std::setw(30) << t.name << "     ║\n";
        std::cout << "╠═════════════════════════════════════════╣\n";
        std::cout << "║  Age      : " << std::setw(25) << (std::to_string(t.age) + " days") << "   ║\n";
        std::cout << "║  Status   : " << std::setw(25) << (t.alive ? "Alive ✓" : "Dead ✗") << "     ║\n";
        std::cout << "╠═════════════════════════════════════════╣\n";

        auto row = [](const std::string& label, int val) {
            std::cout << "║  " << std::left << std::setw(10) << label
                      << ": " << Utils::bar(val, 15)
                      << " " << std::setw(3) << val << "/100"
                      << "  ║\n";
        };

        row("Fullness",  100 - t.hunger);  // inverted: feeding raises bar
        row("Happiness", t.happiness);
        row("Energy",    t.energy);
        row("Health",    t.health);
        std::cout << "╚═════════════════════════════════════════╝\n";
    }

    // Overload 2: display a single stat by label and value
    void display(const std::string& label, int value) {
        std::cout << "[" << label << "] "
                  << Utils::bar(value) << " "
                  << value << "/100"
                  << " (" << Utils::getStatusLabel(value) << ")\n";
    }

    // 2. REFERENCE — parameter & to avoid copying
    void showLogs(const Tamagotchi& t, int lastN = 5) {
        int total = (int)t.logs.size();
        int start = std::max(0, total - lastN);
        std::cout << "\n--- Last " << lastN << " action logs ---\n";
        for (int i = start; i < total; i++) {
            const StatusLog& log = t.logs[i];   // 2. reference to element
            std::cout << "[" << log.timestamp << "] "
                      << std::left << std::setw(8) << log.action
                      << ": " << log.detail << "\n";
        }
        std::cout << "-----------------------------------\n";
    }

    // ─────────────────────────────────────
    // Helper: write string to binary file
    // Format: [uint32_t length][char... data]
    // ─────────────────────────────────────
    inline void writeString(std::ofstream& f, const std::string& s) {
        uint32_t len = static_cast<uint32_t>(s.size());
        f.write(reinterpret_cast<const char*>(&len), sizeof(len));
        f.write(s.data(), len);
    }

    // Helper: read string from binary file
    inline std::string readString(std::ifstream& f) {
        uint32_t len = 0;
        f.read(reinterpret_cast<char*>(&len), sizeof(len));
        std::string s(len, '\0');
        f.read(&s[0], len);
        return s;
    }

    // ─────────────────────────────────────
    // SAVE — write data to binary .sav file
    //
    // File layout (byte order):
    //   MAGIC       4 bytes  "TAMA"         — file identity
    //   VERSION     uint8    1              — format version
    //   name        string   len + chars
    //   hunger      int32    4 bytes
    //   happiness   int32    4 bytes
    //   energy      int32    4 bytes
    //   health      int32    4 bytes
    //   age         int32    4 bytes
    //   alive       uint8    1 byte (0/1)
    //   log_count   uint32   4 bytes
    //   [per log]
    //     timestamp string
    //     action    string
    //     detail    string
    // ─────────────────────────────────────
    bool saveGame(const Tamagotchi& t, const std::string& filename = "tamagotchi.sav") {
        std::ofstream file(filename, std::ios::binary);
        if (!file.is_open()) {
            std::cout << "[!] Failed to open file for saving: " << filename << "\n";
            return false;
        }

        // Magic header + version
        const char magic[4] = {'T','A','M','A'};
        const uint8_t version = 1;
        file.write(magic, 4);
        file.write(reinterpret_cast<const char*>(&version), sizeof(version));

        // Primitive data — write raw bytes directly
        writeString(file, t.name);
        file.write(reinterpret_cast<const char*>(&t.hunger),    sizeof(t.hunger));
        file.write(reinterpret_cast<const char*>(&t.happiness), sizeof(t.happiness));
        file.write(reinterpret_cast<const char*>(&t.energy),    sizeof(t.energy));
        file.write(reinterpret_cast<const char*>(&t.health),    sizeof(t.health));
        file.write(reinterpret_cast<const char*>(&t.age),       sizeof(t.age));
        uint8_t alive = t.alive ? 1 : 0;
        file.write(reinterpret_cast<const char*>(&alive),       sizeof(alive));

        // Log entries
        uint32_t logCount = static_cast<uint32_t>(t.logs.size());
        file.write(reinterpret_cast<const char*>(&logCount), sizeof(logCount));
        for (const StatusLog& log : t.logs) {   // 2. REFERENCE — const ref
            writeString(file, log.timestamp);
            writeString(file, log.action);
            writeString(file, log.detail);
        }

        file.close();
        std::cout << "[✓] Game saved (binary) to \"" << filename << "\"\n";
        return true;
    }

    // ─────────────────────────────────────
    // LOAD — read data from binary .sav file
    // Returns a new pointer, nullptr on failure
    // ─────────────────────────────────────
    Tamagotchi* loadGame(const std::string& filename = "tamagotchi.sav") {
        std::ifstream file(filename, std::ios::binary);
        if (!file.is_open()) {
            std::cout << "[!] Save file not found: " << filename << "\n";
            return nullptr;
        }

        // Validate magic header
        char magic[4];
        file.read(magic, 4);
        if (magic[0]!='T' || magic[1]!='A' || magic[2]!='M' || magic[3]!='A') {
            std::cout << "[!] Save file is invalid or corrupted.\n";
            return nullptr;
        }

        // Validate version
        uint8_t version = 0;
        file.read(reinterpret_cast<char*>(&version), sizeof(version));
        if (version != 1) {
            std::cout << "[!] Save file version not supported (v" << (int)version << ").\n";
            return nullptr;
        }

        // 3. POINTER — allocate new object for loaded data
        Tamagotchi* t = new Tamagotchi();

        t->name = readString(file);
        file.read(reinterpret_cast<char*>(&t->hunger),    sizeof(t->hunger));
        file.read(reinterpret_cast<char*>(&t->happiness), sizeof(t->happiness));
        file.read(reinterpret_cast<char*>(&t->energy),    sizeof(t->energy));
        file.read(reinterpret_cast<char*>(&t->health),    sizeof(t->health));
        file.read(reinterpret_cast<char*>(&t->age),       sizeof(t->age));
        uint8_t alive = 0;
        file.read(reinterpret_cast<char*>(&alive), sizeof(alive));
        t->alive = (alive == 1);

        uint32_t logCount = 0;
        file.read(reinterpret_cast<char*>(&logCount), sizeof(logCount));
        for (uint32_t i = 0; i < logCount; i++) {
            StatusLog log;
            log.timestamp = readString(file);
            log.action    = readString(file);
            log.detail    = readString(file);
            t->logs.push_back(log);
        }

        file.close();
        std::cout << "[✓] Game loaded (binary) from \"" << filename << "\"\n";
        Utils::addLog(*t, "LOAD", "Data loaded — day " + std::to_string(t->age));
        return t;
    }

    // Check whether a save file exists
    inline bool saveExists(const std::string& filename = "tamagotchi.sav") {
        std::ifstream f(filename);
        return f.good();
    }

